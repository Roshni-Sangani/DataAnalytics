#  How memory is managed in Python? 

"""
Python uses an automatic memory management system that  involves dynamic typing, reference counting, 
and garbage collection. 

These components work together to efficiently allocate and deallocate memory during the program's execution.

Heap Memory: Python objects (like integers, strings, lists, etc.) are stored in the heap (dynamic memory).

Stack Memory: Local variables (such as function arguments) and function calls are managed in the stack.
"""