#  How can you pick a random item from a list or tuple?

import random

# Example with a list
my_list = ['apple', 'banana', 'cherry']
random_item = random.choice(my_list)
print(random_item)



# Example with a tuple
my_tuple = ('dog', 'cat', 'rabbit')
random_item = random.choice(my_tuple)
print(random_item)
