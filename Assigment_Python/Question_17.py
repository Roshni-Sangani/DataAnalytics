# What are negative indexes and why are they used? 
"""

Negative indexes are a feature in Python that allow you to access elements of a sequence 
(like lists, strings, or tuples) from the end, rather than the beginning.

How Negative Indexing Works:

In Python, a sequence's elements are indexed starting from 0 for the first element.

A negative index counts from the end, where -1 refers to the last element, -2 refers to the 
second last, and so on.

Example:

my_list = [10, 20, 30, 40, 50]

print(my_list[-1])  # Output: 50 (Last element)
print(my_list[-2])  # Output: 40 (Second last element)
"""